#include "libft.h"

char	*ft_strrev(char *str)
{
	int i;
	int j;
	int k;

	i = 0;
	j = 0;
	k = 0;
	while (str[i] != '\0')
		i++;
	i--;
	while (j < i)
	{
		k = str[i];
		str[i] = str[j];
		str[j] = k;
		i--;
		j++;
	}
	return (str);
}
