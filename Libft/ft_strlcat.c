#include "libft.h"

size_t		ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	j;

	i = ft_strlen(dst);
	j = ft_strlen(src);
	if (i >= size)
		return (j + size);
	ft_strncat(dst, src, size - i - 1);
	return (i + j);
}
